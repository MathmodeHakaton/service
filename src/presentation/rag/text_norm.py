"""
Лёгкая нормализация русского/смешанного текста для retriever'а.

Без внешних зависимостей (pymorphy и stanza тащить ради 30 чанков — оверкилл).
Делаем три простых, но важных вещи:

1. Морф-стемминг: рубим частые окончания (Porter-like, упрощённый список под РФ-финтех).
   «репо/репах/репах», «модуль/модули/модулях» → одна форма.

2. Биграммы: помимо отдельных токенов индексируем пары соседних слов.
   «конец месяца», «налоговая неделя», «репо ЦБ», «недоспрос ОФЗ» — каждая ловится
   как единое понятие. Сильно повышает recall на multi-word концепциях.

3. Entity-extraction: годы (2014..2026), месяцы (январь..декабрь),
   модули (М1..M5), известные термины (RUONIA, ОФЗ, репо, ключевая, и т.д.).
   Идут как явные теги — буст в скоринге +5 за каждое совпадение.
"""
from __future__ import annotations

import re
from typing import Iterable, Set, Tuple

# Русские стоп-слова + английские служебные. Минимальный набор.
_STOP = frozenset({
    "и", "в", "во", "на", "по", "с", "со", "для", "из", "от", "до",
    "что", "как", "это", "за", "под", "над", "при", "у", "о", "об",
    "же", "ли", "не", "ни", "а", "но", "или", "то", "если", "так",
    "тут", "вот", "был", "была", "было", "есть", "быть", "там", "тут",
    "он", "она", "оно", "они", "его", "её", "их", "ему", "ей",
    "этот", "эта", "это", "эти", "тот", "та", "те",
    "мы", "вы", "ты", "я", "мне", "нам", "вам",
    "the", "of", "in", "on", "is", "are", "to", "and", "a", "an",
    "что-то", "чтобы", "почему", "когда",
})

# Окончания, которые срезаем. Длиннее — приоритет.
_SUFFIXES = sorted([
    # существительные
    "иями", "ями", "иям", "ями", "ами", "иях", "ях", "ах", "ам", "ям",
    "ого", "ему", "ому", "ыми", "ами",
    "ов", "ёв", "ев", "ой", "ою", "ей", "ие", "ия", "ии", "ию", "ью",
    # прилагательные/причастия
    "ая", "яя", "ое", "ее", "ые", "ие", "ый", "ий", "ой",
    # глаголы (упрощённо)
    "ться", "тся", "ете", "ишь", "ёшь", "ешь",
    "ть", "ла", "ло", "ли", "ал", "ил", "ел", "ну",
    # короткие
    "ы", "и", "е", "у", "ю", "а", "я", "о", "ь",
], key=len, reverse=True)

_TOKEN_RE = re.compile(r"[\wа-яёА-ЯЁ]+", re.UNICODE)

_MONTH_PATTERNS: dict[str, str] = {
    # короткий маркер месяца → стандартизованное имя (как у нас в крис-эпизодах).
    "январ": "jan",  "феврал": "feb", "март":   "mar",
    "апрел": "apr",  "май":    "may", "мая":    "may", "июн": "jun",
    "июл":   "jul",  "август": "aug", "сентябр": "sep",
    "октябр": "oct", "ноябр":  "nov", "декабр": "dec",
}

_MODULE_PATTERNS = re.compile(
    r"\b(?:м|m)\s*([1-5])\b|\b([1-5])-?\s*(?:й|го)?\s*модул",
    re.IGNORECASE | re.UNICODE,
)

_YEAR_RE = re.compile(r"\b(19|20)\d{2}\b")

# Известные термины проекта — буст при матче. Все в нормализованной форме.
_DOMAIN_KEYWORDS = frozenset({
    "ruonia", "руониа",
    "ofz", "офз",
    "репо", "cover",
    "ключев", "ставк",
    "минфин", "цб", "роскаазн", "казначейств", "бюджет",
    "недоспрос", "переспрос",
    "shap", "catboost",
    "статус", "красн", "жёлт", "жёлт", "зелён", "норма",
    "стресс", "пик", "максим", "минимум",
    "налог", "квартал", "месяц",
    "методолог", "модул", "признак", "фич",
    "ликвидн", "корсчёт", "корсчет", "резерв",
    "lsi", "лси",
    "сейчас", "текущ", "последн",
})


def stem(word: str) -> str:
    """Лёгкий стемминг. Не идеально, но достаточно для матча.
    Не трогаем короткие слова (<5 букв) — они и так компактные."""
    w = word.lower()
    if len(w) <= 4:
        return w
    for suf in _SUFFIXES:
        if len(w) - len(suf) >= 3 and w.endswith(suf):
            return w[: -len(suf)]
    return w


def tokenize(text: str) -> list[str]:
    if not text:
        return []
    out = []
    for raw in _TOKEN_RE.findall(text):
        low = raw.lower()
        if low in _STOP or len(low) < 2:
            continue
        out.append(stem(low))
    return out


def bigrams(tokens: list[str]) -> list[str]:
    return [f"{a}_{b}" for a, b in zip(tokens, tokens[1:])]


def extract_entities(text: str) -> Set[str]:
    """Из произвольного текста извлекает явные сущности: годы, месяцы, модули,
    известные ключевые слова. Возвращает набор тегов вида:
        '2022', 'mar', 'M1', 'kw:недоспрос', ...
    """
    tags: Set[str] = set()
    t = text or ""

    # Годы
    for m in _YEAR_RE.finditer(t):
        tags.add(m.group(0))

    # Месяцы — ищем префикс в нижнем регистре
    low = t.lower()
    for stem_, code in _MONTH_PATTERNS.items():
        if stem_ in low:
            tags.add(code)

    # Модули M1..M5 в разной нотации
    for mm in _MODULE_PATTERNS.finditer(t):
        digit = mm.group(1) or mm.group(2)
        if digit:
            tags.add(f"M{digit}")

    # Доменные ключевые слова — матчим по корням
    for kw in _DOMAIN_KEYWORDS:
        if kw in low:
            tags.add(f"kw:{kw}")

    return tags


def tokens_and_grams(text: str) -> Tuple[list[str], list[str]]:
    toks = tokenize(text)
    return toks, bigrams(toks)
